    <p class="align_justify">
        Olen Jesse Siekkinen ja opiskelen Jyväskylän ammattikorkeakoulussa (<a href="http://www.jamk.fi">jamk.fi</a>) tieto- ja viestintätekniikkaa.
        Tavoitteena on valmistua neljän vuoden kuluessa kyseisen alan insinööriksi erikoistuen kyberturvallisuuteen.
        Minulla on vahva harrastuspohja tietotekniikan saralla jo ennestään ja aikaisemmin olen hankkinut sekä sähkö- että automaatioasentajan pätevyyden.
        Ammateista ja pätevyyksistäni sekä koulutuksestani lisää <a href="/fi/koulutus/">koulutus</a>-sivulla.
