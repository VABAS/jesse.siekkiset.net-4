    <p class="align_justify">
        Harrastukseni ovat äkkiseltään katsottuna värittyneet moneen suuntaan.
        Kuitenkin lähes jokaisessa yhdistävänä asiana on että tekniikka liittyy niihin tai koko harrastus perustuu tekniikkaan.
        Harrastuksiini kuuluukin ohjelmointia, web-koodausta ym. tietotekniikka näpräystä, valokuvausta, radiotekniikkaa, teatteri-/esitystekniikkaa ja pianon soittoa.
        Harrastuksistani tarkemmin <a href="/fi/harrastukset/">harrastussivulla</a>.
    </p>
    <p class="align_justify">
